import UIKit

extension UIColor {
    @nonobjc class var black: UIColor {
        return UIColor(white: 50.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var warmGrey: UIColor {
        return UIColor(white: 155.0 / 255.0, alpha: 1.0)
    }

    @nonobjc class var blackTwo: UIColor {
        return UIColor(white: 33.0 / 255.0, alpha: 0.72)
    }

    @nonobjc class var blackThree: UIColor {
        return UIColor(white: 23.0 / 255.0, alpha: 1.0)
    }
}
